package com.boot.model;

public class StudentRegistration {
	private int age;
	private String name;

	public int getAge() {
		return this.age;
	}

	public String getName() {
		return this.name;
	}

	public void setAge(final int age) {
		this.age = age;
	}

	public void setName(final String name) {
		this.name = name;
	}
}
